var i = `Hello, World!
This is a multi-line string.
Have a great day!`

console.log(i);

// Output:
// Hello, World!
// This is a multi-line string.
// Have a great day!        

// The backticks (`) allow for multi-line strings in JavaScript.

// You can also include expressions using ${expression} syntax.

var name1 = "Alice"
var greeting = `Hello, ${name1}!
Welcome to the multi-line string example.` 

console.log(name1)
console.log(greeting)

